/**
 * POST /functions/v1/spotify-auth
 * Handles OAuth 2.0 with PKCE for Spotify connection.
 */
export interface SpotifyAuthRequest {
  action: 'connect' | 'callback' | 'disconnect';
  // connect: returns redirect_url
  // callback: receives code + code_verifier, exchanges for tokens
  // disconnect: sets is_active = false
  code?: string;
  code_verifier?: string;
}

export interface SpotifyAuthResponse {
  success: boolean;
  redirect_url?: string;         // for action=connect
  error?: string;
}

/**
 * POST /functions/v1/spotify-sync
 * Fetches top artists, top tracks, audio features from Spotify.
 * Computes aggregates. Atomically upserts spotify_profiles.
 * Called on initial connect + every 30 days via cron.
 */
export interface SpotifySyncRequest {
  user_id: string;               // target user (cron passes this; client omits it)
}

export interface SpotifySyncResponse {
  success: boolean;
  synced_at?: string;
  error?: string;
}

/**
 * POST /functions/v1/portrait-generate
 * Reads spotify_profiles + weighted books, calls Claude Haiku,
 * validates output (80–400 chars), stores in spotify_profiles.portrait_text.
 * One retry on failure; retains previous value if retry fails.
 */
export interface PortraitGenerateRequest {
  user_id: string;
  manual?: boolean;              // if true, checks 7-day throttle
}

export interface PortraitGenerateResponse {
  success: boolean;
  portrait_text?: string;
  mood_label?: MoodLabel;
  mood_line?: string;
  error?: string;
}

/**
 * POST /functions/v1/reading-themes
 * Reads primary-signal books, calls Claude Haiku, validates JSON array
 * of 2–5 strings (each <40 chars), stores in profiles.reading_themes.
 * Triggered at 3 books, then every 3 new books (3, 6, 9…).
 */
export interface ReadingThemesRequest {
  user_id: string;
}

export interface ReadingThemesResponse {
  success: boolean;
  themes?: string[];             // 2–5 theme strings
  error?: string;
}

/**
 * POST /functions/v1/bookshelf-scan
 * Receives a bookshelf photo, sends to Gemini Flash Vision API,
 * returns extracted titles and authors.
 * Rate limit: max 3 attempts per user per 24 hours.
 */
export interface BookshelfScanRequest {
  image_base64: string;          // base64-encoded photo
}

export interface BookshelfScanResponse {
  success: boolean;
  books?: { title: string; author: string | null }[];
  error?: string;
}

/**
 * POST /functions/v1/book-enrich
 * Given title + author, fetches cover, genres, description from Google Books API.
 * Used after card save, review submit, bookshelf import, and Goodreads CSV import.
 */
export interface BookEnrichRequest {
  title: string;
  author?: string;
}

export interface BookEnrichResponse {
  success: boolean;
  cover_url?: string;
  genres?: string[];
  description?: string;
  google_books_id?: string;
  error?: string;
}

/**
 * POST /functions/v1/goodreads-import
 * Receives CSV content, parses rows where Exclusive Shelf = "read",
 * enriches each via Google Books, upserts into books table.
 * Duplicate detection: title + author match → skip.
 * CSV is never stored server-side.
 */
export interface GoodreadsImportRequest {
  csv_content: string;           // raw CSV text
}

export interface GoodreadsImportResponse {
  success: boolean;
  imported_count: number;
  skipped_count: number;
  error?: string;
}


