export const MOOD_QUADRANTS = {
  Euphoric:    { valence: '> 0.6', energy: '> 0.6' },
  Peaceful:    { valence: '> 0.6', energy: '<= 0.6' },
  Intense:     { valence: '<= 0.6', energy: '> 0.6' },
  Melancholic: { valence: '<= 0.6', energy: '<= 0.6' },
} as const;

export type MoodLabel = keyof typeof MOOD_QUADRANTS;


