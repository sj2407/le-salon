/**
 * spotify_profiles — one row per user, upserted on each sync
 * PK is user_id (not a separate id column).
 */
export interface SpotifyProfile {
  user_id: string;                // UUID, PK, FK → profiles.id
  synced_at: string;              // timestamptz — last successful sync
  is_active: boolean;             // false = disconnected (soft delete)

  // Tokens — encrypted at rest, never exposed to client
  spotify_access_token: string;
  spotify_refresh_token: string;

  // Aggregates computed server-side during sync
  top_artists: SpotifyArtist[];   // jsonb — top 10
  top_genres: GenreCount[];       // jsonb — mapped vocabulary
  avg_valence: number;            // 0–1
  avg_energy: number;             // 0–1
  avg_tempo: number;              // BPM
  pct_minor: number;              // 0–1
  avg_acousticness: number;       // 0–1
  listening_mode: 'immersion' | 'explorer';
  cultural_geography: RegionCount[]; // jsonb

  // Mood — derived from valence/energy quadrant, no LLM needed
  mood_label: 'Euphoric' | 'Peaceful' | 'Intense' | 'Melancholic';
  mood_line: string;              // e.g. "minor key · contemplative · slow"

  // Portrait prose — generated by Claude Haiku
  portrait_text: string | null;
  portrait_generated_at: string | null;
  last_portrait_manual_at: string | null; // for 7-day throttle
}

export interface SpotifyArtist {
  name: string;
  image_url: string | null;
  genres: string[];               // raw Spotify genre strings
}

export interface GenreCount {
  genre: string;                  // mapped label from spotify-genre-map.json
  count: number;
}

export interface RegionCount {
  region: string;
  count: number;
}

/**
 * books — one row per book per user.
 * NEW TABLE. Populated from card entries, reviews, bookshelf import, Goodreads CSV.
 * Separate from reviews table — a book can exist without a review.
 */
export interface Book {
  id: string;                     // UUID, PK
  user_id: string;                // UUID, FK → profiles.id
  title: string;                  // NOT NULL
  author: string | null;

  // Google Books enrichment
  cover_url: string | null;       // cached from Google Books
  google_books_id: string | null;
  google_books_genres: string[] | null; // jsonb
  google_books_description: string | null;

  // Goodreads enrichment (from CSV import)
  goodreads_genres: string[] | null; // jsonb

  // Status & source
  status: 'reading' | 'read' | 'want_to_read';
  source: 'card' | 'review' | 'bookshelf_import' | 'goodreads_csv';

  // Rating — synced from reviews table (0–10 scale, matching existing reviews)
  rating: number | null;
  review_id: string | null;       // UUID, FK → reviews.id, nullable

  created_at: string;
}

/**
 * experiences — past cultural events displayed in Portrait.
 * Separate from the activities table (which tracks upcoming events on the Activity Board).
 */
export interface Experience {
  id: string;                     // UUID, PK
  user_id: string;                // UUID, FK → profiles.id
  name: string;                   // NOT NULL
  category: ExperienceCategory;
  date: string | null;            // date (not timestamptz)
  city: string | null;
  note: string | null;
  source: 'activity_board' | 'manual';
  created_at: string;
}

export type ExperienceCategory =
  | 'concert'
  | 'exhibition'
  | 'restaurant'
  | 'cinema'
  | 'theatre'
  | 'other';

/**
 * creations — user-authored original work (poems, screenshots, photos, writing).
 */
export interface Creation {
  id: string;                     // UUID, PK
  user_id: string;                // UUID, FK → profiles.id
  type: 'text' | 'image';
  title: string | null;           // max 80 chars
  text_content: string | null;    // max 2000 chars, for type="text"
  image_url: string | null;       // Supabase Storage URL, for type="image"
  is_visible: boolean;            // DEFAULT true — eye toggle
  created_at: string;
}

/**
 * profiles extension — columns added to the existing profiles table.
 */
export interface ProfilePortraitExtension {
  reading_themes: string[] | null;  // jsonb — ["memory & loss", "post-colonial voices", ...]
  reading_themes_at: string | null; // timestamptz — last theme generation
}


