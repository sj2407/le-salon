/**
 * Existing reviews table uses 0–10 scale.
 * PRD weighting logic uses 5-star language. Mapping:
 *
 *   0–10 range  | PRD equivalent | Signal weight
 *   -----------|----------------|--------------------------------------
 *   8–10        | 4–5★ (high)    | Primary signal — always included
 *   5–7         | 3★ (mid)       | Deprioritized (no review) or included (with review)
 *   1–4         | 1–2★ (low)     | Excluded without review, included with review
 *   0 or null   | Unrated        | Deprioritized
 */
export const RATING_HIGH_MIN = 8;
export const RATING_MID_MIN = 5;
export const RATING_LOW_MIN = 1;

/**
 * Signal weighting for portrait generation (worktree-2 uses this logic):
 *
 *   Status            | Rating    | Has review? | Weight
 *   ------------------|-----------|-------------|----------------------------
 *   read              | 8–10      | yes         | PRIMARY — always included
 *   read              | 8–10      | no          | PRIMARY — clean positive signal
 *   read              | 1–4       | yes         | INCLUDED — review explains it
 *   read              | 5–7       | yes         | INCLUDED
 *   read              | 5–7       | no          | DEPRIORITIZED
 *   read              | 1–4       | no          | EXCLUDED — ambiguous
 *   read              | unrated   | no          | DEPRIORITIZED
 *   reading           | n/a       | n/a         | CONTEXT — "currently reading"
 *   bookshelf_import  | unrated   | n/a         | BACKGROUND — context only
 */


